def task_if1(): # Дано номер року (додатне ціле число). Визначити кількість днів в цьому році,
# враховуючи, що звичайний рік нараховує 365 днів, а високосний – 366 днів.
    try:
        year = int(input("Введіть рік (додатне ціле число): "))
        if year > 0:
            # Перевірка, чи є рік високосним
            if (year % 4 == 0 and year % 100 != 0) or (year % 400 == 0):
                days = 366
            else:
                days = 365
            print(f"У {year} році {days} днів.")
        else:
            print("Рік повинен бути додатним числом!")
    except ValueError:
        print("ПОМИЛКА: очікувалося ціле число!")


