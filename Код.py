def task_geom4(): # Дано дійсні числа (xi, yi), i = 1,2, ... n, – координати точок на
# площині. Визначити кількість точок, що потрапляють в червону область

    # Введення параметрів фігури
    a = float(input("Введіть a (довжину катета): "))
    r = float(input("Введіть r (радіус кола): "))

    # Центр кола
    cx = a - r * math.sqrt(2)
    cy = 0

    # Введення кількості точок
    n = int(input("Введіть кількість точок n: "))

    # Перевірка кожної точки
    for i in range(n):
        print(f"\nТочка №{i+1}:")
        x = float(input("  Введіть x: "))
        y = float(input("  Введіть y: "))

        # Перевірка належності точки до червоної області
        in_circle = (x - cx)**2 + (y - cy)**2 <= r**2
        in_triangle = (x >= 0) and (y >= 0) and (x + y <= a)

        # Перевірка положення точки
        if in_circle and in_triangle and y > 0:
            print("  Точка в верхній половині кола, що входить у трикутник (червона область).")
        elif in_circle and y < 0:
            print("  Точка в нижній половині кола.")
        else:
            print("  Точка не надлежить жодній з областей.")

