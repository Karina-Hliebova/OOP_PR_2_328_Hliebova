def task_matline6(): # Дослідити ряд на збіжність. Умова закінчення циклу обчислення
#суми прийняти у вигляді: | un | <E або | un | > G де е – мала величина для
#переривання циклу обчислення суми сходиться ряду (е = 10-5... 10-20); g –
#величина для переривання циклу обчислення суми розходиться ряду (g = 102...105).

    print("Дослідження ряду: u_n = (3 * n * n!) / n^n")
    # Введення параметрів
    # Якщо користувач нічого не введе — беруться значення за замовчуванням.
    e = float(input("Введіть малу величину e (наприклад 1e-10): ") or 1e-10)
    g = float(input("Введіть велику величину g (наприклад 1e+5): ") or 1e+5)

    n = 1 # лічильник номеру члена ряду
    u = (3 * n * math.factorial(n)) / (n ** n) # перший член ряду
    s = u # початкова сума ряду

    while True:
        if abs(u) < e: # Перевірка умови збіжності
            print("\nРяд сходиться.") # якщо член дуже малий — ряд збігається
            print(f"Сума ряду ≈ {s:.10f}") 
            return True
        elif abs(u) > g: # Перевірка умови розбіжності
            print("\nРяд розбігається.") # якщо член занадто великий — ряд розбігається
            print(f"Максимальна сума (до розбіжності): {s:.10f}")
            return False

        n += 1
        try:  # Обчислення нового члена за тією ж формулою
            u = (3 * n * math.factorial(n)) / (n ** n)
        except OverflowError:  # Якщо число стало надто великим для обчислення
            print("Обчислення вийшло за межі можливого!")
            return False
        except Exception as err:
            print(f"Помилка при обчисленні u: {err}")
            return False

        s += u

